package org.javaprograms.Task2;
public class Basic_VariableExplorer {
    public static void main(String[] args) {
        int myAge = 32;
        float f = 1.23F;
        double d = 1.1245;
        char c = 'a';
        boolean isThatTrue = false;

        String myName = "AppiumTester";

        System.out.println(myAge);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(isThatTrue);
        System.out.println("I am a "+myName);
        //System.out.printf("I am a %s",myName);
    }
}
